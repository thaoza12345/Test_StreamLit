    # ‡∏Å‡∏≥‡∏´‡∏ô‡∏î‡∏™‡∏µ Marker ‡∏ï‡∏≤‡∏° search_term
    marker_color = "red" if "‡∏ö‡∏≤‡∏Ñ‡∏≤‡∏£‡πà‡∏≤" in row['search_term'].lower() or "‡∏û‡∏ô‡∏±‡∏ô" in row['search_term'].lower() else "blue" # ‡πÄ‡∏û‡∏¥‡πà‡∏° '‡∏û‡∏ô‡∏±‡∏ô' ‡πÅ‡∏•‡∏∞‡∏ó‡∏≥‡πÉ‡∏´‡πâ‡πÄ‡∏õ‡πá‡∏ô lowercase ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÄ‡∏ó‡∏µ‡∏¢‡∏ö
    folium.Marker(
        location=[row['latitude'], row['longitude']],
        popup=f"IP: {row['ip']}<br>Term: {row['search_term']}<br>Time: {row['timestamp'].strftime('%Y-%m-%d %H:%M')}",
        icon=folium.Icon(color=marker_color)
    ).add_to(m)
