# Chart 1: Bar Chart (Count of IPs per search term)
st.subheader("üìä ‡∏à‡∏≥‡∏ô‡∏ß‡∏ô IP ‡∏ï‡πà‡∏≠‡∏Ñ‡∏≥‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤")
